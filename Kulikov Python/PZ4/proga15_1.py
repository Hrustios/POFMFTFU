#Дано вещественное число А и целое число № (>0). Используя один цикл, найти сумму 1+A+A^2+ A^3 + ... + A^N. 
try:
    # Вводим вещественное число A
    A = float(input("Введите вещественное число A: "))
    
    # Вводим целое число N, которое должно быть больше 0
    N = int(input("Введите целое число N (>0): "))
    
    # Проверяем, что N больше 0, иначе вызовем исключение ValueError
    if N <= 0:
        raise ValueError("Число N должно быть больше 0.")
    
    # Инициализируем переменную для хранения суммы
    sum_result = 0
    
    # Инициализируем переменную для текущего члена последовательности, начиная с A^0 = 1
    current_term = 1
    
    # Используем цикл для вычисления суммы степеней от A^0 до A^N
    for i in range(N + 1):  # Проходим от 0 до N включительно
        sum_result += current_term  # Добавляем текущий член к сумме
        current_term *= A  # Переходим к следующему члену, умножая на A
    
    # Выводим итоговую сумму
    print("Сумма:", sum_result)

# Обрабатываем исключение ValueError для некорректных данных (например, если N <= 0)
except ValueError as e:
    print("Ошибка:", e)
