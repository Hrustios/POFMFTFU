#Дано целое число N (>0), являющееся некоторой степенью числа 2: N = 2^К. Найти целое число К- показатель этой степени.
try:
    # Вводим целое число N
    N = int(input("Введите целое число N (>0), являющееся степенью числа 2: "))
    
    # Проверяем, что N является положительным и степенью числа 2
    if N <= 0 or (N & (N - 1)) != 0:
        raise ValueError("Число N должно быть положительным и степенью числа 2.")
    
    # Инициализируем переменную K для подсчета показателя степени
    K = 0
    
    # Пока N больше 1, делим его на 2 и увеличиваем K на 1
    while N > 1:
        N //= 2  # Целочисленное деление на 2, уменьшаем N
        K += 1   # Увеличиваем показатель степени
    
    # Выводим значение показателя степени K
    print("Показатель степени K:", K)

# Обрабатываем исключение ValueError для некорректных данных
except ValueError as e:
    print("Ошибка:", e)

# Ловим любые другие ошибки, которые могут возникнуть
except Exception as e:
    print("Произошла непредвиденная ошибка:", e)
