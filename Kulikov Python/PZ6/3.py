#Дан список размера N. Осуществить циклический сдвиг элементов списка влево на одну позицию (при этом АN перейдет в АN-1, AN-1 - в AN-2,..., А1 - в АN).
def left_shift(lst):
    try:
        if not lst:
            print("Список пуст, сдвиг невозможен.")
            return
        lst = lst[1:] + lst[:1]  # Сдвиг: от второго элемента до конца + первый элемент
        print("Результат:", lst)
    except Exception:
        print("Ошибка обработки списка.")

# Пример вызова
lst = [1, 2, 3, 4, 5]
left_shift(lst)
